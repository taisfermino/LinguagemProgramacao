public class HelloJava {
    
    /*
        O método "main" indica o "ponto de entrada",
        ou seja, indica para o Java por onde o programa deve começar a ser
        executado.
    
        Geralmente, temos um método main para cada software que escrevemos.
        Em aula, teremos vários, e faremos isso para fins didáticos.
    
        Os atalhos para criá-lo:
            - main <TAB> || <ENTER>
            - psvm <TAB> || <ENTER>
    */
    
    
    public static void main(String[] args) {
        
        System.out.println("Hello World!");
        
        /*
            O comando "println" da classe System, permite a exibição de
            mensagens no console.
        
            Como argumento, podemos passar qualquer coisa desde que retorne
            algo que possa ser convertido para texto.
        */
        
    }
}
